namespace OmronFinsNetStandard.Enums
{
    /// <summary>
    /// Represents the state of a bit, either on or off.
    /// </summary>
    public enum BitState
    {
        /// <summary>
        /// The bit is turned on.
        /// </summary>
        ON = 1,

        /// <summary>
        /// The bit is turned off.
        /// </summary>
        OFF = 0
    }
}
