namespace OmronFinsNetStandard.Enums
{
    /// <summary>
    /// Represents the data types used in PLC memory.
    /// </summary>
    public enum DataType
    {
        /// <summary>
        /// Single bit data type.
        /// </summary>
        BIT,

        /// <summary>
        /// 16-bit integer data type.
        /// </summary>
        INT16,

        /// <summary>
        /// 32-bit floating-point data type.
        /// </summary>
        REAL
    }
}
